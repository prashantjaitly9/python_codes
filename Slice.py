# Accessing characters
text = "Prashant"
first_char = text[0]
last_char = text[-1]
print("First character:", first_char)  # Output: First character: P
print("Last character:", last_char)    # Output: Last character: n

# Slicing
substring = text[1:4]
print("Substring (1:4):", substring)   # Output: Substring (1:4): yth

print(f"{text} Jaitly")